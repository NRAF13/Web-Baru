<h1 class="popo ungu tebal text-center wow fadeInLeft" data-wow-delay="2.8s">PERATURAN KHUSUS ROLEPLAY</h1>
<p class="popo putih wow fadeInLeft" data-wow-delay="2.9s">1.1 Roleplay<br>
Roleplay adalah sebuah mode permainan dimana kalian akan memilih peran yang kalian jalani sesuai dengan keinginan kalian yang akan kalian gunakan ke karakter yang kalian buat. Kalian akan mengendalikan karakter yang kalian buat seperti layaknya di dunia nyata, kalian harus bekerja untuk mendapatkan uang, kalian harus bersosialisasi untum mendapatkan teman di dalam game.  Dan kalian diharuskan memerankan karakter kalian se-realistis mungkin(seperti sayang nyawa dan harta).</p>
<p class="popo putih wow fadeInLeft" data-wow-delay="2.9s">1.2 OOC<br>
OOC atau Out Of Character adalah segala hal yang tidak ada hubungannya sama sekali dengan karakter kalain di dalam game.</p>
<p class="popo putih wow fadeInLeft" data-wow-delay="2.9s">1.3 IC<br>
IC atau In Character adalah segala hal yang tidak ada hubungannya sama sekali dengan dengan kehidupan kalian di dunia nyata.</p>
<p class="popo putih wow fadeInLeft" data-wow-delay="2.9s">1.4 Breakrules(Dilarang)<br>
Breakrules adalah tindakan yang melanggar seluruh peraturan yang berlaku.</p>
<p class="popo putih wow fadeInLeft" data-wow-delay="2.9s">1.5 MetaGaming(Dilarang)<br>
MetaGaming adalah pemanfaatan fitur yang tidak seharusnya misalkan: Kalian memanggil nama seseorang dengan melihat nametag diatas kepala tanpa berkenalan terlebih dahulu.</p>
<p class="popo putih wow fadeInLeft" data-wow-delay="2.9s">1.6 Mixxing(Dilarang)<br>
Mixxing adalah mencampur adukan informasi OOC ke IC untuk kepentingan pribadi. Misalnya: Kalian mengetahui pekerjaan seseorang hanya dengan melihat role di discord.</p>
<p class="popo putih wow fadeInLeft" data-wow-delay="2.9s">1.7 PowerGaming(Dilarang)<br>
PowerGaming adalah suatu kondisi dimana kalian melakukan aktifitas yang tidak mungkin terjadi di dunia nyata seperti sehabis ditabrak tetapi kalian tetap bisa jalan normal seperti biasa. PowerGaming juga dapat berarti memaksakan kehendak pribadi kalian selama ber-RP.</p>
<p class="popo pitih wow fadeInLeft" data-wow-delay="2.9s">1.8 Non-RP Name(Dilarang)<br>
Menggunakan nama yang tidak masuk akal dan tidak sesuai dengan dunia nyata seperti menggunakan nama Aku_Tampan, Raja_Barong. Penggunaan nama tidak masuk akal hanya dapat digunakan di fitur sosial media seperti twitter dll.</p>
<p class="popo putih wow fadeInLeft" data-wow-delay="2.9s">1.9 Famous Name(Dilarang)<br>
Penggunaan nama dari artis atau pun tokoh ternama dilarang seperti Marion_Jola, Joko_Widodo.</p>
<p class="popo putih wow fadeInLeft" data-wow-delay="2.9s">1.10 Non-RP(Dilarang)<br>
Adalah segala tindakan yang tidak menggunakan dan memainkan rp dengan benar.</p>
